def find_smallest_square():
    l = 987654321
    x, y = N, N

    for idx in range(M):
        # íƒˆì¶œí•œ ëŸ¬ë„ˆëŠ” ë³¼ í•„ìš” ì—†ë‹¤.
        if left[idx]:
            r, c = runners[idx]
            # í˜„ì¬ ì°¾ì€ ê°€ì¥ ì§§ì€ ê±°ë¦¬ì™€ ê°™ê±°ë‚˜ ë” ì§§ì•„ì•¼ ë³¼ ê°€ì¹˜ê°€ ìˆë‹¤.
            # ì¶œêµ¬ì™€ í–‰, ì—´ì´ ê°™ì€ ê²½ìš°ëŠ” ë‹¤ë¥¸ ê²½ìš°ê°€ ê±°ë¦¬ê°€ ë˜ì–´ì¤„ ê²ƒì´ë©°
            # ëŒ€ê°ì„ ì¸ ê²½ìš° ì§§ì€ê±¸ë¡œ í•˜ë©´ ê¸´ê±¸ ì»¤ë²„ ëª»í•˜ë‹ˆê¹ ê²°êµ­ ê¸´ê±¸ë¡œ í•´ì•¼í•œë‹¤.
            # ë”°ë¼ì„œ ëŒ€ê°ì„ ì— ìˆë“  ì§ì„ ì— ìˆë“  ê²°êµ­ í° ì°¨ì´ê°€ ë‚˜ëŠ” ê±¸ë¡œ ê¸¸ì´ë¥¼ ì¬ì•¼í•œë‹¤.
            # ì°¨ì´ + 1ì„ í•´ì•¼ ì •ì‚¬ê°í˜•ì˜ ê¸¸ì´ê°€ ë‚˜ì˜¨ë‹¤.
            length = max(abs(e_row - r), abs(e_col - c)) + 1
            if length > l:
                continue

            # ì¢Œìƒì„ êµ¬í•œë‹¤.
            r1, c1 = min(e_row, r), min(e_col, c)
            # ìš°í•˜ë¥¼ êµ¬í•œë‹¤.
            r2, c2 = max(e_row, r), max(e_col, c)

            # ëŒ€ê°ì„ ì¸ ê²½ìš°
            # [ğŸ¤¬] ëŒ€ê°ì„ ì´ ì•„ë‹Œ ê²½ìš°ë§Œ êµ¬í–ˆë”ë‹ˆ ì‹œì‘ ì§€ì ì„ ì œëŒ€ë¡œ ëª» ì°¾ìŒ
            if e_row != r and e_col != c:
                # í–‰ ì°¨ì´ê°€ ë” í¬ë‹¤ -> ì—´ì„ ì˜ ê³¨ë¼ì•¼í•¨
                if abs(e_row - r) > abs(e_col - c):
                    # í–‰ ì°¨ì´ëŠ” ê·¸ ìì²´ê°€ ê¸¸ì´ê°€ ë˜ì—ˆìœ¼ë¯€ë¡œ, í–‰ì€ ê³ ì • ë¨
                    # ì´ì œ ì¢€ ë” ì™¼ìª½ì—ì„œ ì‹œì‘í•˜ëŠ” ì—´ì„ ì°¾ì•„ì•¼í•¨
                    # ê·¼ë° ë” ì˜¤ë¥¸ìª½ì— ìˆëŠ” ì—´(c2)ì—ì„œ ê¸¸ì´ë¥¼ ëº€ ê°’ì´ 0ë³´ë‹¤ í¬ë©´ ê±°ê¸°ì„œ ì‹œì‘í•˜ë©´ ë¨
                    # ê·¼ë° 0ì„ ë„˜ì–´ê°€ ìŒìˆ˜ê°€ ë˜ì–´ë²„ë¦¬ë©´ ê·¸ëƒ¥ 0ë¶€í„° ì‹œì‘í•˜ë©´ ë¨
                    # [ğŸ¤¬] ì—¬ê¸°ë„ ê·¸ëƒ¥ 0ìœ¼ë¡œ ë°”ê¿€ì§€ ë§ì§€ë§Œ ê³„ì‚°í–ˆìŒ. c1, c2ë„ ì•„ë‹Œ ê°’ì´ c1ì— ë“¤ì–´ê°€ì•¼í•  ìˆ˜ ìˆìŒ
                    c1 = c2 - length + 1 if c2 - length + 1 > 0 else 0
                # ì—´ ì°¨ì´ê°€ ë” í¬ë‹¤ -> í–‰ì„ ì˜ ê³¨ë¼ì•¼í•¨
                elif abs(e_row - r) < abs(e_col - c):
                    # ì—´ ì°¨ì´ëŠ” ê·¸ ìì²´ê°€ ê¸¸ì´ê°€ ë˜ì—ˆìœ¼ë¯€ë¡œ, ì—´ì€ ê³ ì • ë¨
                    # ì´ì œ ì¢€ ë” ìœ„ì—ì„œ ì‹œì‘í•˜ëŠ” í–‰ì„ ê³¨ë¼ì•¼í•¨
                    # ë” ì•„ë˜ì— ìˆëŠ” í–‰(r2)ì—ì„œ ê¸¸ì´ë¥¼ ëº€ ê°’ì´ 0ë³´ë‹¤ í¬ë©´ ê±°ê¸°ì„œ ì‹œì‘í•˜ë©´ ë¨
                    # ê·¼ë° 0ì„ ë„˜ì–´ ìŒìˆ˜ê°€ ë˜ì–´ë²„ë¦¬ë©´ ê·¸ëƒ¥ 0ë¶€í„° ì‹œì‘í•˜ë©´ ë¨
                    # [ğŸ¤¬] ì—¬ê¸°ë„ ê·¸ëƒ¥ 0ìœ¼ë¡œ ë°”ê¿€ì§€ ë§ì§€ë§Œ ê³„ì‚°í–ˆìŒ. r1, r2ë„ ì•„ë‹Œ ê°’ì´ r1ì— ë“¤ì–´ê°€ì•¼í•  ìˆ˜ ìˆìŒ
                    r1 = r2 - length + 1 if r2 - length + 1 > 0 else 0
                # ì°¨ì´ê°€ ê°™ìœ¼ë©´ ê·¸ëƒ¥ ì¢Œìƒì„ ë°˜í™˜í•˜ë©´ ë¨
            # í–‰ì´ ê°™ì„ë•Œ, 0 ë˜ëŠ” r1 - length ì¤‘ ê³¨ë¼ì•¼í•œë‹¤.
            elif e_row == r:
                # ë§Œì•½ r1ì—ì„œ lengthë¥¼ ëº€ê²Œ 0í–‰ë³´ë‹¤ ë°‘ì— ìˆìœ¼ë©´ ê±°ê¸°ì„œ ì‹œì‘í•˜ëŠ”ê²Œ ì‘ì€ ì •ì‚¬ê°í˜•ì„ ë§Œë“¤ ìˆ˜ ìˆë‹¤.
                # [ğŸ¤¬] ì—¬ê¸°ë„ ê·¸ëƒ¥ 0ìœ¼ë¡œ ë°”ê¿€ì§€ ë§ì§€ë§Œ ê³„ì‚°í–ˆìŒ. r1, r2ë„ ì•„ë‹Œ ê°’ì´ r1ì— ë“¤ì–´ê°€ì•¼í•  ìˆ˜ ìˆìŒ
                r1 = r1 - length + 1 if r1 - length + 1 > 0 else 0
            # ì—´ì´ ê°™ì€ë°, 0 ë˜ëŠ” c1 - length ì¤‘ ê³¨ë¼ì•¼í•œë‹¤.
            elif e_col == c:
                # ë§Œì•½ ì—´ì—ì„œ ê¸¸ì´ë¥¼ ëº€ê²Œ 0ì—´ë³´ë‹¤ ì˜¤ë¥¸ìª½ì— ìˆìœ¼ë©´ ê±°ê¸°ì„œ ì‹œì‘í•˜ëŠ”ê²Œ ì‘ì€ ì •ì‚¬ê°í˜•ì„ ë§Œë“¤ ìˆ˜ ìˆë‹¤.
                # [ğŸ¤¬] ì—¬ê¸°ë„ ê·¸ëƒ¥ 0ìœ¼ë¡œ ë°”ê¿€ì§€ ë§ì§€ë§Œ ê³„ì‚°í–ˆìŒ. c1, c2ë„ ì•„ë‹Œ ê°’ì´ c1ì— ë“¤ì–´ê°€ì•¼í•  ìˆ˜ ìˆìŒ
                c1 = c1 - length + 1 if c1 - length + 1 > 0 else 0

            # ê¸¸ì´ê°€ ì ê±°ë‚˜, ê°™ì€ë° ì •ì‚¬ê°í˜•ì˜ ì‹œì‘ ìœ„ì¹˜ê°€ ë” ìœ„ or ì™¼ìª½ì¸ ê²½ìš° ì—…ë°ì´íŠ¸
            if length < l or x > r1 or (x == r1 and y > c1):
                l = length
                x, y = r1, c1

    return l, x, y


# ì‹œê³„ë°©í–¥ìœ¼ë¡œ 90ë„ íšŒì „í•œë‹¤. (r,c) => (c, l - r - 1)
# íšŒì „ëœ ë²½ì€ ë‚´êµ¬ë„ê°€ 1ì”© ê¹Œì¸ë‹¤.
def rotate_maze(l, x, y):
    global e_row, e_col
    rotated = [[0] * l for _ in range(l)]

    # ì—‡ ì‚¬ëŒì€...?
    for i in range(M):
        if left[i] and x <= runners[i][0] <= x + l - 1 and y <= runners[i][1] <= y + l - 1:
            # [ğŸ¤¬] ëŸ¬ë„ˆ ì´ë™ë„ ëŒ€ì¶© ê³„ì‚°í•¨
            s_row = runners[i][0] - x
            s_col = runners[i][1] - y
            runners[i] = [x + s_col, y + l - s_row - 1]

    # ëŒë¦¬ê¸°
    for i in range(l):
        for j in range(l):
            # [ğŸ¤¬] ì¸ë±ìŠ¤ ì˜¤ë¥˜ ì—„ì²­ ë‚¨. ê·¼ë° ì‹œê³„ë°©í–¥ ê³µì‹ ë¬¸ì œê°€ ì•„ë‹ˆë¼ ìœ„ì—ì„œ ì •ì‚¬ê°í˜• ì‹œì‘ì  ë§Œë“œëŠ”ê±°ì—ì„œ ë¬¸ì œê°€ ìê¾¸ ë‚¨
            rotated[j][l - 1 - i] = maze[x + i][y + j]
            if 1 <= rotated[j][l - i - 1] <= 9:
                rotated[j][l - i - 1] -= 1
            # íƒˆì¶œêµ¬ë„ ëŒë ¤ì§€ë©´ ì—…ë°ì´íŠ¸
            if maze[x + i][y + j] == -1:
                e_row, e_col = x + j, y + l - i - 1

    # ëŒë¦°ê±° ë‹¤ì‹œ ì €ì¥
    for i in range(l):
        for j in range(l):
            maze[x + i][y + j] = rotated[i][j]


def solution():
    global K
    # Kì´ˆë¥¼ ë°˜ë³µí•˜ê±°ë‚˜, ëª¨ë“  ì°¸ê°€ìê°€ íƒˆì¶œí•œë‹¤ë©´
    while K:
        move()
