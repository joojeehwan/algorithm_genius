# ì…ë ¥ ì²˜ë¦¬

N, M, K = map(int, input().split())
maze = list(list(map(int, input().split())) for _ in range(N))

runners = list()
for i in range(M):
    r_row, r_col = map(int, input().split())
    # ì…ë ¥ì— ì¥ë‚œì§ˆ ì¢€ stop...
    runners.append([r_row - 1, r_col - 1])

e_row, e_col = map(int, input().split())
# ì…ë ¥ì— ì¥ë‚œì§ˆ ì¢€ stop...
e_row -= 1
e_col -= 1
maze[e_row][e_col] = -1

# ì „ì—­ ë³€ìˆ˜
answer = 0  # ëŸ¬ë„ˆë“¤ì´ ì›€ì§ì¸ ê±°ë¦¬
left = [True] * M  # í•´ë‹¹ ëŸ¬ë„ˆê°€ ë¯¸ë¡œì— ë‚¨ì•„ìˆëŠ”ì§€
dr = [-1, 1, 0, 0]  # ìƒ í•˜ ì¢Œ ìš°
dc = [0, 0, -1, 1]


def in_range(_r, _c):
    return 0 <= _r < N and 0 <= _c < N


# ëª¨ë“  ëŸ¬ë„ˆê°€ ë™ì‹œì— ì›€ì§ì¸ë‹¤.
def move():
    global answer
    for idx in range(M):
        # íƒˆì¶œí–ˆëŠ”ì§€ í™•ì¸í•œë‹¤.
        if left[idx]:
            r, c = runners[idx]
            # ìƒí•˜ì¢Œìš°ë¡œ 1ì¹¸ ì›€ì§ì¼ ìˆ˜ ìˆë‹¤.
            for d in range(4):
                # ìƒ, í•˜ë¡œ ì›€ì§ì´ëŠ” ê²ƒì„ ìš°ì„ ì‹œí•œë‹¤.
                nr = r + dr[d]
                nc = c + dc[d]

                # íƒˆì¶œí•œ ê²½ìš°
                if e_row == nr and e_col == nc:
                    left[idx] = False
                    # [ğŸ¤¬] íƒˆì¶œí•œ ê²ƒë„ ì›€ì§ì¸ ê±°ë¦¬ì— í¬í•¨í•´ì•¼ë¨
                    answer += 1
                    continue
                if not in_range(nr, nc):
                    continue
                # ë²½ì´ ìˆëŠ” ê³³ì—ëŠ” ê°ˆ ìˆ˜ ì—†ë‹¤.
                if 1 <= maze[nr][nc] <= 9:
                    continue
                # í˜„ì¬ ë¨¸ë¬¼ë €ë˜ ì¹¸ ë³´ë‹¤ ì¶œêµ¬ê°€ ê°€ê¹Œì›Œì•¼í•œë‹¤.
                if abs(e_row - r) + abs(e_col - c) < abs(e_row - nr) + abs(e_col - nc):
                    continue
                # ì›€ì§ì¼ ìˆ˜ ì—†ë‹¤ë©´ ì›€ì§ì´ì§€ ì•ŠëŠ”ë‹¤.
                runners[idx] = [nr, nc]
                answer += 1
                break


